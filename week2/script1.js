// Function to change text color to Red
function turnRed() {
    // Find the paragraph by its ID
    var element = document.getElementById("myParagraph");
    // Change the color style to red
    element.style['color'] = "red";
}

// Function to change text color to Blue
function turnBlue() {
    var element = document.getElementById("myParagraph");
    element.style['color'] = "blue";
}